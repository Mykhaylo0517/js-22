const oneSecond = 1;
const qSecondsInHour = 60 * oneSecond;
const qSecondsInDay = 24 * qSecondsInHour;
const qSecondsInMonth = 30 * qSecondsInDay;
document.writeln(`There are ${qSecondsInHour} seconds in a hour.`);
document.writeln(`There are ${qSecondsInDay} seconds in a day. `);
document.writeln(`There are ${qSecondsInMonth} seconds in a month. `);